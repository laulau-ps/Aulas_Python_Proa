# laço for em variavel

''' usado para iterar em uma sequência, ou seja, podemos executar um conjunto de intruções, uma vez para cada item em uma lista, tupla, dict, etc
O for não precisa de variavel de indexação (contadora)'''

# exemplo:

frutas = ['maçã', 'banana', 'cereja']

for i in frutas:
    print(i)

#exibe cada dado do vetor 'frutas'    