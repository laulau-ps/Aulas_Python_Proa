
''' A informação pode ser passada para funções como argumentos.

Os argumentos são especificados após o nome da função, dentro dos parênteses. Você pode adicionar quantos argumentos quiser, só separe com uma vírgula.'''

def funcao_nomes(primeiro_nome): # parametro
    print(primeiro_nome + " Silva")
    
funcao_nomes("Laura") # argumento   
funcao_nomes("João")    
funcao_nomes("Maria")    


""" Do ponto de vista de uma função:

Um parâmetro é a variável listada dentro dos parênteses na definição da função.

Um argumento é o valor real que é enviado à função quando ela é chamada. """