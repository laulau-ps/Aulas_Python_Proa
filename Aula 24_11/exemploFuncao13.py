
''' Funções podem retornar valores usando a afirmação:return'''

def minha_func(x, y):
    return x + y #retorna o cálculo dos parametros x e y

resultado = minha_func(5, 3) #guarda na variavel 'resultado' o retorno da função com os argumentos de x e y (sendo assim possível de realizar o cálculo)
print(resultado) #exibe a variavel 




