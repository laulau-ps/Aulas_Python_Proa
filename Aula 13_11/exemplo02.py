# Slicing de strings em python

b = "Hello, World"

print(b[2:5])
#exibe as letras presente entre 2 e 5

print(b[:5])
#exibe as letras do inicio até a posição 5

print(b[2:])
#exibe a partir da posição 2 até o final

print(b[-5:-2])
#posições negativas servem para cortar iniciando no final da string, ou seja, inicia a exibição a partir da posição 5 (como se a 5 se tornasse o inicio da string) até a posição 2 